          ),
          const SizedBox(height: 8),
          _buildQuestExample('ÊØéÊúù„Ç∏„Éß„ÇÆ„É≥„Ç∞', Icons.directions_run, theme),
          _buildQuestExample('Ë™≠Êõ∏30ÂàÜ', Icons.book, theme),
