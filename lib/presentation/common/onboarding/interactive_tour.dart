import 'package:flutter/material.dart';
import 'package:minq/presentation/common/onboarding/onboarding_engine.dart';

/// „Ç§„É≥„Çø„É©„ÇØ„ÉÜ„Ç£„Éñ„ÉÑ„Ç¢„Éº„ÇíË°®Á§∫„Åô„Çã„Çπ„ÇØ„É™„Éº„É≥
class InteractiveTourScreen extends StatefulWidget {
  final List<TourStep> steps;
  final VoidCallback? onComplete;

  const InteractiveTourScreen({
    super.key,
    required this.steps,
    this.onComplete,
  });

  @override
  State<InteractiveTourScreen> createState() => _InteractiveTourScreenState();
}

class _InteractiveTourScreenState extends State<InteractiveTourScreen>
    with TickerProviderStateMixin {
  int _currentStepIndex = 0;
  late AnimationController _slideController;
  late AnimationController _fadeController;
  late Animation<Offset> _slideAnimation;
  late Animation<double> _fadeAnimation;

  @override
  void initState() {
    super.initState();
    _initializeAnimations();
    _fadeController.forward();
  }

  void _initializeAnimations() {
    _slideController = AnimationController(
      duration: const Duration(milliseconds: 300),
      vsync: this,
    );

    _fadeController = AnimationController(
      duration: const Duration(milliseconds: 400),
      vsync: this,
    );

    _slideAnimation = Tween<Offset>(
      begin: const Offset(1.0, 0.0),
      end: Offset.zero,
    ).animate(
      CurvedAnimation(parent: _slideController, curve: Curves.easeInOut),
    );

    _fadeAnimation = Tween<double>(begin: 0.0, end: 1.0).animate(
      CurvedAnimation(parent: _fadeController, curve: Curves.easeInOut),
    );
  }

  void _nextStep() {
    if (_currentStepIndex < widget.steps.length - 1) {
      _slideController.forward().then((_) {
        setState(() {
          _currentStepIndex++;
        });
        _slideController.reset();
      });

      // „Ç´„Çπ„Çø„É†„Ç¢„ÇØ„Ç∑„Éß„É≥„ÇíÂÆüË°å
      widget.steps[_currentStepIndex].onNext?.call();
    } else {
      _completeTour();
    }
  }

  void _previousStep() {
    if (_currentStepIndex > 0) {
      _slideController.forward().then((_) {
        setState(() {
          _currentStepIndex--;
        });
        _slideController.reset();
      });
    }
  }

  void _completeTour() async {
    await OnboardingEngine.markOnboardingCompleted();
    widget.onComplete?.call();
    if (mounted) {
      Navigator.of(context).pop();
    }
  }

  void _skipTour() {
    showDialog(
      context: context,
      builder:
          (context) => AlertDialog(
            title: const Text('„ÉÑ„Ç¢„Éº„Çí„Çπ„Ç≠„ÉÉ„Éó„Åó„Åæ„Åô„ÅãÔºü'),
            content: const Text('Âæå„ÅßË®≠ÂÆöÁîªÈù¢„Åã„Çâ„ÉÑ„Ç¢„Éº„ÇíÂÜçÈñã„Åß„Åç„Åæ„Åô„ÄÇ'),
            actions: [
              TextButton(
                onPressed: () => Navigator.of(context).pop(),
                child: const Text('„Ç≠„É£„É≥„Çª„É´'),
              ),
              TextButton(
                onPressed: () {
                  Navigator.of(context).pop();
                  _completeTour();
                },
                child: const Text('„Çπ„Ç≠„ÉÉ„Éó'),
              ),
            ],
          ),
    );
  }

  @override
  void dispose() {
    _slideController.dispose();
    _fadeController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    final currentStep = widget.steps[_currentStepIndex];
    final theme = Theme.of(context);

    return Scaffold(
      backgroundColor: Colors.black.withOpacity(0.8),
      body: FadeTransition(
        opacity: _fadeAnimation,
        child: SafeArea(
          child: Column(
            children: [
              // „Éò„ÉÉ„ÉÄ„Éº
              _buildHeader(theme),

              // „É°„Ç§„É≥„Ç≥„É≥„ÉÜ„É≥„ÉÑ
              Expanded(
                child: SlideTransition(
                  position: _slideAnimation,
                  child: _buildStepContent(currentStep, theme),
                ),
              ),

              // „Éï„ÉÉ„Çø„Éº
              _buildFooter(theme),
            ],
          ),
        ),
      ),
    );
  }

  Widget _buildHeader(ThemeData theme) {
    return Padding(
      padding: const EdgeInsets.all(16.0),
      child: Row(
        mainAxisAlignment: MainAxisAlignment.spaceBetween,
        children: [
          Text(
            'MinQ „ÉÑ„Ç¢„Éº',
            style: theme.textTheme.headlineSmall?.copyWith(
              color: Colors.white,
              fontWeight: FontWeight.bold,
            ),
          ),
          TextButton(
            onPressed: _skipTour,
            child: Text(
              '„Çπ„Ç≠„ÉÉ„Éó',
              style: TextStyle(color: Colors.white.withOpacity(0.8)),
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildStepContent(TourStep step, ThemeData theme) {
    return Container(
      margin: const EdgeInsets.all(24.0),
      padding: const EdgeInsets.all(24.0),
      decoration: BoxDecoration(
        color: theme.colorScheme.surface,
        borderRadius: BorderRadius.circular(16),
        boxShadow: [
          BoxShadow(
            color: Colors.black.withOpacity(0.2),
            blurRadius: 20,
            offset: const Offset(0, 10),
          ),
        ],
      ),
      child: Column(
        mainAxisSize: MainAxisSize.min,
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // „Çπ„ÉÜ„ÉÉ„Éó„Ç§„É≥„Ç∏„Ç±„Éº„Çø„Éº
          _buildStepIndicator(theme),

          const SizedBox(height: 24),

          // „Çø„Ç§„Éà„É´
          Text(
            step.title,
            style: theme.textTheme.headlineSmall?.copyWith(
              fontWeight: FontWeight.bold,
              color: theme.colorScheme.onSurface,
            ),
          ),

          const SizedBox(height: 16),

          // Ë™¨Êòé
          Text(
            step.description,
            style: theme.textTheme.bodyLarge?.copyWith(
              color: theme.colorScheme.onSurface.withOpacity(0.8),
              height: 1.5,
            ),
          ),

          const SizedBox(height: 24),

          // „Ç´„Çπ„Çø„É†„Ç¶„Ç£„Ç∏„Çß„ÉÉ„Éà
          if (step.customWidget != null) ...[
            step.customWidget!,
            const SizedBox(height: 24),
          ],

          // „Ç§„É≥„Çø„É©„ÇØ„ÉÜ„Ç£„ÉñË¶ÅÁ¥†
          _buildInteractiveElement(step, theme),
        ],
      ),
    );
  }

  Widget _buildStepIndicator(ThemeData theme) {
    return Row(
      children: List.generate(
        widget.steps.length,
        (index) => Container(
          width: 8,
          height: 8,
          margin: const EdgeInsets.only(right: 8),
          decoration: BoxDecoration(
            shape: BoxShape.circle,
            color:
                index <= _currentStepIndex
                    ? theme.colorScheme.primary
                    : theme.colorScheme.outline.withOpacity(0.3),
          ),
        ),
      ),
    );
  }

