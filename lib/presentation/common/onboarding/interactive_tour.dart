    return GestureDetector(
      onTap: () {
        // „Éá„É¢Áî®„ÅÆ„ÉÅ„Çß„ÉÉ„ÇØ„Éú„ÉÉ„ÇØ„Çπ„Ç¢„Éã„É°„Éº„Ç∑„Éß„É≥
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: const Text('Á¥†Êô¥„Çâ„Åó„ÅÑÔºÅ„ÇØ„Ç®„Çπ„ÉàÂÆå‰∫Ü„Åß„Åô üéâ'),
